char getch2(){
   char c=0;
   DWORD modo, contador;
   HANDLE ih = GetStdHandle(STD_INPUT_HANDLE);
   SetConsoleMode (ih, modo & ~(ENABLE_ECHO_INPUT | ENABLE_LINE_INPUT));
   ReadConsoleA (ih, &c, 1, &contador, NULL);
   if (c==0) {
      ReadConsoleA (ih, &c, 1, &contador, NULL);
   }
   SetConsoleMode (ih, modo);
   return c;
}
 
